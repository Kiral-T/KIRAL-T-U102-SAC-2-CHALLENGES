for i in range(1, 13):  # Outer loop for each times table (1 to 12)
    print(i, "Times Table\n")
    for j in range(1, 13):  # Inner loop for multiplying 1 to 12
        print(i, "times", j, "=", i * j)
    print()  # Blank line after each table
